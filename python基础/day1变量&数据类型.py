"""
编写一个程序，实现以下功能：
a. 提示用户输入一个0到65535之间的十进制整数。
b. 验证输入有效性（是否为整数且在范围内），若无效则提示并重新输入。
c. 分别计算并打印该数字的二进制、八进制、十六进制表示（使用内置函数，带前缀）。
d. 尝试将该数字视为Unicode码点，使用chr()函数获取对应的字符，并打印。
e. 获取该字符的UTF-8编码字节串（使用encode()），并分别以十六进制形式
（如b‘\xe4\xb8\xad‘的显示样式）和十进制字节值列表的形式打印出来。
示例运行（假设输入20320）：
请输入一个0-65535之间的十进制整数： 20320 
十进制： 20320 
二进制： 0b100111101100000 
八进制： 0o47540 
十六进制： 0x4f60 
对应字符： 你 UTF-8编码字节串（十六进制样式）： b‘\xe4\xbd\xa0‘ 
UTF-8编码字节值列表： [228， 189， 160] 
请提交完整的Python代码。
"""

num = 0
while(True):
    input_num = input("请输入一个0-65535之间的十进制整数：")
    num = int(input_num)
    if 0 <= num <= 65535:
        break
    else:
        print("输入超出范围，请重新输入！")
print("十进制：%d"%num)
print("二进制：%s"%bin(num))
print("八进制：%s"%oct(num))
print("十六进制：%s"%hex(num))
print("对应字符： 你 UTF-8编码字节串（十六进制样式）：",chr(num).encode(encoding='utf8'))
lists = list(chr(num).encode(encoding='utf8'))
print("UTF-8编码字节值列表：",lists)

